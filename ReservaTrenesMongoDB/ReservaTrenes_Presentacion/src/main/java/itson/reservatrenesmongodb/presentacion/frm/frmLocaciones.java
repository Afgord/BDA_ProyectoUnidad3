    private void btnEditarLocacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarLocacionActionPerformed
        int filaSeleccionada = tblListaLocaciones.getSelectedRow();

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(
                    this,
                    "Seleccione una locación de la tabla para editarla.",
                    "Sin selección",
                    JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        DefaultTableModel modelo
                = (DefaultTableModel) tblListaLocaciones.getModel();

        idLocacionEnEdicion = modelo.getValueAt(filaSeleccionada, 0).toString();

        txtClaveLocacion.setText(
                modelo.getValueAt(filaSeleccionada, 1).toString()
        );

        txtNombreLocacion.setText(
                modelo.getValueAt(filaSeleccionada, 2).toString()
        );

        txtEstadoLocacion.setText(
                modelo.getValueAt(filaSeleccionada, 3).toString()
        );

        txtPaisLocacion.setText(
                modelo.getValueAt(filaSeleccionada, 4).toString()
        );

        chkLocacionActiva.setSelected(
                "Activa".equalsIgnoreCase(
                        modelo.getValueAt(filaSeleccionada, 5).toString()
                )
        );

        lblAgregarLocacion.setText("Editar locación");
        btnAgregarLocacion.setText("Actualizar");
        txtClaveLocacion.requestFocus();
    }//GEN-LAST:event_btnEditarLocacionActionPerformed

    private void btnEliminarLocacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarLocacionActionPerformed
        int filaSeleccionada = tblListaLocaciones.getSelectedRow();

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(
                    this,
                    "Seleccione una locación de la tabla para eliminarla.",
                    "Sin selección",
                    JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        int confirmacion = JOptionPane.showConfirmDialog(
                this,
                "¿Desea eliminar la locación seleccionada?",
                "Confirmar eliminación",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE
        );

        if (confirmacion != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            DefaultTableModel modelo
                    = (DefaultTableModel) tblListaLocaciones.getModel();

            String idLocacion = modelo.getValueAt(filaSeleccionada, 0).toString();

            ILocacionServicio locacionServicio = new LocacionServicio();
            locacionServicio.eliminar(idLocacion);

            JOptionPane.showMessageDialog(
                    this,
                    "Locación eliminada correctamente.",
                    "Eliminación exitosa",
                    JOptionPane.INFORMATION_MESSAGE
            );

            if (idLocacion.equals(idLocacionEnEdicion)) {
                limpiarFormulario();
            }

            cargarLocaciones();

        } catch (ServicioException e) {
            JOptionPane.showMessageDialog(
                    this,
                    e.getMessage(),
                    "No fue posible eliminar la locación",
                    JOptionPane.WARNING_MESSAGE
            );
        }
    }//GEN-LAST:event_btnEliminarLocacionActionPerformed


