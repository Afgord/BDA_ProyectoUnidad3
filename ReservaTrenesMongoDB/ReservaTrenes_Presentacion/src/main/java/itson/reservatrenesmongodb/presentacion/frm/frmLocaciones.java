    /**
     * Configura las columnas de la tabla de locaciones.
     *
     * La columna ID se conserva internamente para futuras operaciones de
     * edición y eliminación, pero se oculta visualmente.
     */
    private void configurarTablaLocaciones() {
        DefaultTableModel modelo = new DefaultTableModel(
                new Object[]{
                    "ID",
                    "CLAVE",
                    "NOMBRE",
                    "ESTADO",
                    "PAÍS",
                    "ESTATUS"
                },
                0
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        tblListaLocaciones.setModel(modelo);

        // Oculta visualmente la columna ID, pero sigue disponible en el modelo.
        tblListaLocaciones.getColumnModel().getColumn(0).setMinWidth(0);
        tblListaLocaciones.getColumnModel().getColumn(0).setMaxWidth(0);
        tblListaLocaciones.getColumnModel().getColumn(0).setWidth(0);
    }

    /**
     * Carga las locaciones registradas en la tabla desde la capa de servicios.
     */
    private void cargarLocaciones() {
        try {
            ILocacionServicio locacionServicio = new LocacionServicio();
            List<LocacionDTO> locaciones = locacionServicio.consultarTodas();

            DefaultTableModel modelo
                    = (DefaultTableModel) tblListaLocaciones.getModel();

            modelo.setRowCount(0);

            for (LocacionDTO locacion : locaciones) {
                modelo.addRow(new Object[]{
                    locacion.getId(),
                    locacion.getClave(),
                    locacion.getNombre(),
                    locacion.getEstado(),
                    locacion.getPais(),
                    locacion.isActiva() ? "Activa" : "Inactiva"
                });
            }

            lblLocacionesRegistradas.setText(
                    locaciones.size() + " locaciones registradas"
            );

        } catch (ServicioException e) {
            JOptionPane.showMessageDialog(
                    this,
                    "No fue posible cargar las locaciones.\n"
                    + e.getMessage(),
                    "Error al cargar locaciones",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }

    /**
     * Registra una nueva locación a partir de los datos del formulario.
     */
    private void registrarLocacion() {
        try {
            LocacionDTO locacionDTO = new LocacionDTO(
                    txtClaveLocacion.getText(),
                    txtNombreLocacion.getText(),
                    txtEstadoLocacion.getText(),
                    txtPaisLocacion.getText(),
                    chkLocacionActiva.isSelected()
            );

            ILocacionServicio locacionServicio = new LocacionServicio();
            locacionServicio.registrar(locacionDTO);

            JOptionPane.showMessageDialog(
                    this,
                    "Locación registrada correctamente.",
                    "Registro exitoso",
                    JOptionPane.INFORMATION_MESSAGE
            );

            limpiarFormulario();
            cargarLocaciones();

        } catch (ServicioException e) {
            JOptionPane.showMessageDialog(
                    this,
                    e.getMessage(),
                    "No fue posible registrar la locación",
                    JOptionPane.WARNING_MESSAGE
            );
        }
    }

    /**
     * Actualiza la locación que se encuentra actualmente en edición.
     */
    private void actualizarLocacion() {
        try {
            ILocacionServicio locacionServicio = new LocacionServicio();

            LocacionDTO locacionDTO = locacionServicio.buscarPorId(
                    idLocacionEnEdicion
            );

            if (locacionDTO == null) {
                JOptionPane.showMessageDialog(
                        this,
                        "La locación seleccionada ya no existe.",
                        "Locación no encontrada",
                        JOptionPane.WARNING_MESSAGE
                );

                limpiarFormulario();
                cargarLocaciones();
                return;
            }

            locacionDTO.setClave(txtClaveLocacion.getText());
            locacionDTO.setNombre(txtNombreLocacion.getText());
            locacionDTO.setEstado(txtEstadoLocacion.getText());
            locacionDTO.setPais(txtPaisLocacion.getText());
            locacionDTO.setActiva(chkLocacionActiva.isSelected());

            locacionServicio.actualizar(locacionDTO);

            JOptionPane.showMessageDialog(
                    this,
                    "Locación actualizada correctamente.",
                    "Actualización exitosa",
                    JOptionPane.INFORMATION_MESSAGE
            );

            limpiarFormulario();
            cargarLocaciones();

        } catch (ServicioException e) {
            JOptionPane.showMessageDialog(
                    this,
                    e.getMessage(),
                    "No fue posible actualizar la locación",
                    JOptionPane.WARNING_MESSAGE
            );
        }
    }

    /**
     * Limpia los campos del formulario de locaciones.
     */
    private void limpiarFormulario() {
        txtClaveLocacion.setText("");
        txtNombreLocacion.setText("");
        txtEstadoLocacion.setText("");
        txtPaisLocacion.setText("");
        chkLocacionActiva.setSelected(true);

        idLocacionEnEdicion = null;
        lblAgregarLocacion.setText("Agregar nueva locación");
        btnAgregarLocacion.setText("+ Agregar");

        tblListaLocaciones.clearSelection();
        txtClaveLocacion.requestFocus();
    }
