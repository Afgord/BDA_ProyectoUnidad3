    /**
     * Configura las columnas de la tabla de pasajeros.
     *
     * La columna ID se mantiene oculta para utilizarla posteriormente al
     * consultar el historial del pasajero seleccionado.
     */
    private void configurarTablaPasajeros() {
        DefaultTableModel modelo = new DefaultTableModel(
                new Object[]{
                    "ID",
                    "NOMBRE COMPLETO",
                    "TELÉFONO",
                    "CORREO ELECTRÓNICO",
                    "CIUDAD",
                    "VIAJES REGISTRADOS"
                },
                0
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        tblListaPasajeros.setModel(modelo);

        tblListaPasajeros.getColumnModel().getColumn(0).setMinWidth(0);
        tblListaPasajeros.getColumnModel().getColumn(0).setMaxWidth(0);
        tblListaPasajeros.getColumnModel().getColumn(0).setWidth(0);

        tblListaPasajeros.setAutoResizeMode(javax.swing.JTable.AUTO_RESIZE_OFF);

        tblListaPasajeros.getColumnModel().getColumn(1).setPreferredWidth(220);
        tblListaPasajeros.getColumnModel().getColumn(2).setPreferredWidth(130);
        tblListaPasajeros.getColumnModel().getColumn(3).setPreferredWidth(240);
        tblListaPasajeros.getColumnModel().getColumn(4).setPreferredWidth(160);
        tblListaPasajeros.getColumnModel().getColumn(5).setPreferredWidth(160);
    }

    /**
     * Carga los pasajeros registrados en la tabla.
     */
    private void cargarPasajeros() {
        try {
            IPasajeroServicio pasajeroServicio = new PasajeroServicio();
            List<PasajeroDTO> pasajeros = pasajeroServicio.consultarTodos();

            DefaultTableModel modelo
                    = (DefaultTableModel) tblListaPasajeros.getModel();

            modelo.setRowCount(0);

            for (PasajeroDTO pasajero : pasajeros) {
                modelo.addRow(new Object[]{
                    pasajero.getId(),
                    pasajero.getNombreCompleto(),
                    pasajero.getTelefono(),
                    pasajero.getCorreo(),
                    pasajero.getCiudad(),
                    pasajero.getViajesRegistrados()
                });
            }

            lblContadorPasajeros.setText(
                    pasajeros.size() + " pasajeros registrados"
            );

        } catch (ServicioException e) {
            JOptionPane.showMessageDialog(
                    this,
                    "No fue posible cargar los pasajeros.\n"
                    + e.getMessage(),
                    "Error al cargar pasajeros",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }

