    private void btnMostrarHistorialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarHistorialActionPerformed
        int fila = tblListaPasajeros.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(
                    this,
                    "Seleccione un pasajero de la tabla.",
                    "Sin selección",
                    JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        String pasajeroId = tblListaPasajeros.getValueAt(fila, 0).toString();
        String nombrePasajero = tblListaPasajeros.getValueAt(fila, 1).toString();

        java.awt.Window ventanaPadre = SwingUtilities.getWindowAncestor(this);
        java.awt.Frame framePadre = ventanaPadre instanceof java.awt.Frame
                ? (java.awt.Frame) ventanaPadre
                : null;

        dlgHistorialPasajero dialogo = new dlgHistorialPasajero(
                framePadre,
                true,
                pasajeroId,
                nombrePasajero
        );

        dialogo.setVisible(true);
    }//GEN-LAST:event_btnMostrarHistorialActionPerformed


