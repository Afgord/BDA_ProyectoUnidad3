    private void btnBuscarViajeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarViajeActionPerformed
        try {
            LocacionDTO origen = obtenerLocacionSeleccionada(
                    cmbOrigenConsultaViajes
            );

            LocacionDTO destino = obtenerLocacionSeleccionada(
                    cmbDestinoConsultaViajes
            );

            String origenId = origen != null ? origen.getId() : null;
            String destinoId = destino != null ? destino.getId() : null;
            String fechaSalida = obtenerFechaSalidaSeleccionada();
            String tipoBoleto = obtenerTipoBoletoSeleccionado();

            IViajeServicio viajeServicio = new ViajeServicio();

            List<ViajeDisponibleDTO> viajesDisponibles
                    = viajeServicio.consultarViajesDisponibles(
                            origenId,
                            destinoId,
                            fechaSalida,
                            tipoBoleto
                    );

            cargarResultadosBusqueda(viajesDisponibles);

        } catch (ServicioException e) {
            limpiarResultadosBusqueda();

            JOptionPane.showMessageDialog(
                    this,
                    e.getMessage(),
                    "Validación",
                    JOptionPane.WARNING_MESSAGE
            );
        }
    }//GEN-LAST:event_btnBuscarViajeActionPerformed


