    /**
     * Carga las locaciones disponibles en los combos de origen y destino.
     */
    private void cargarLocaciones() {
        try {
            ILocacionServicio locacionServicio = new LocacionServicio();

            locacionesDisponibles = locacionServicio.consultarTodas();

            cmbOrigenConsultaViajes.removeAllItems();
            cmbDestinoConsultaViajes.removeAllItems();

            cmbOrigenConsultaViajes.addItem("Seleccionar");
            cmbDestinoConsultaViajes.addItem("Seleccionar");

            for (LocacionDTO locacion : locacionesDisponibles) {
                String textoLocacion = locacion.getClave()
                        + " - "
                        + locacion.getNombre();

                cmbOrigenConsultaViajes.addItem(textoLocacion);
                cmbDestinoConsultaViajes.addItem(textoLocacion);
            }

            cmbOrigenConsultaViajes.setSelectedIndex(0);
            cmbDestinoConsultaViajes.setSelectedIndex(0);

        } catch (ServicioException e) {
            JOptionPane.showMessageDialog(
                    this,
                    "No fue posible cargar las locaciones.\n"
                    + e.getMessage(),
                    "Error al cargar locaciones",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }

