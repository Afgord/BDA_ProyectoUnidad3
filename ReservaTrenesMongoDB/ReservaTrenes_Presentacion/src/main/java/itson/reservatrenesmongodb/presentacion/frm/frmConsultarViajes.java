    private void btnComprarBoletoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprarBoletoActionPerformed
        int filaSeleccionada = tblListaConsultarViajes.getSelectedRow();

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(
                    this,
                    "Seleccione un viaje de la tabla.",
                    "Sin selección",
                    JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        int filaModelo = tblListaConsultarViajes.convertRowIndexToModel(
                filaSeleccionada
        );

        ViajeDisponibleDTO viajeSeleccionado
                = resultadosBusqueda.get(filaModelo);

        String tipoBoletoInicial = obtenerTipoBoletoSeleccionado();

        java.awt.Window ventanaPadre
                = javax.swing.SwingUtilities.getWindowAncestor(this);

        java.awt.Frame framePadre
                = ventanaPadre instanceof java.awt.Frame
                        ? (java.awt.Frame) ventanaPadre
                        : null;

        dlgComprarBoleto dialogo = new dlgComprarBoleto(
                framePadre,
                true,
                viajeSeleccionado,
                tipoBoletoInicial
        );

        dialogo.setVisible(true);
    }//GEN-LAST:event_btnComprarBoletoActionPerformed

