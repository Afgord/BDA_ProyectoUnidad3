    private void btnAgregarServicioTrenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarServicioTrenActionPerformed
        Object itemSeleccionado = cmbServicioTren.getEditor().getItem();

        String servicio = itemSeleccionado == null
                ? ""
                : itemSeleccionado.toString().trim();

        if (servicio.isBlank()) {
            JOptionPane.showMessageDialog(
                    this,
                    "Seleccione o escriba un servicio antes de agregarlo.",
                    "Servicio vacío",
                    JOptionPane.WARNING_MESSAGE
            );
            cmbServicioTren.requestFocus();
            return;
        }

        for (int i = 0; i < modeloServiciosTren.size(); i++) {
            if (modeloServiciosTren.getElementAt(i).equalsIgnoreCase(servicio)) {
                JOptionPane.showMessageDialog(
                        this,
                        "Ese servicio ya fue agregado al tren.",
                        "Servicio duplicado",
                        JOptionPane.WARNING_MESSAGE
                );
                cmbServicioTren.requestFocus();
                return;
            }
        }

        modeloServiciosTren.addElement(servicio);
        cmbServicioTren.setSelectedItem(null);
        cmbServicioTren.requestFocus();
    }//GEN-LAST:event_btnAgregarServicioTrenActionPerformed

    private void btnEliminarServicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarServicioActionPerformed
        int indiceSeleccionado = listServiciosTren.getSelectedIndex();

        if (indiceSeleccionado == -1) {
            JOptionPane.showMessageDialog(
                    this,
                    "Seleccione un servicio de la lista para quitarlo.",
                    "Sin selección",
                    JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        modeloServiciosTren.remove(indiceSeleccionado);
    }//GEN-LAST:event_btnEliminarServicioActionPerformed


