    /**
     * Configura las columnas de la tabla de trenes.
     *
     * La columna ID se mantiene oculta para usarla después en edición y
     * eliminación.
     */
    private void configurarTablaTrenes() {
        DefaultTableModel modelo = new DefaultTableModel(
                new Object[]{
                    "ID",
                    "CÓDIGO",
                    "NOMBRE",
                    "MODELO",
                    "ESTATUS",
                    "CAP. GENERAL",
                    "CAP. PRIMERA",
                    "SERVICIOS"
                },
                0
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        tblListaTrenes.setModel(modelo);

        tblListaTrenes.getColumnModel().getColumn(0).setMinWidth(0);
        tblListaTrenes.getColumnModel().getColumn(0).setMaxWidth(0);
        tblListaTrenes.getColumnModel().getColumn(0).setWidth(0);
    }

    /**
     * Carga los trenes registrados en la tabla.
     */
    private void cargarTrenes() {
        try {
            ITrenServicio trenServicio = new TrenServicio();
            List<TrenDTO> trenes = trenServicio.consultarTodos();

            DefaultTableModel modelo
                    = (DefaultTableModel) tblListaTrenes.getModel();

            modelo.setRowCount(0);

            for (TrenDTO tren : trenes) {
                modelo.addRow(new Object[]{
                    tren.getId(),
                    tren.getCodigo(),
                    tren.getNombre(),
                    tren.getModelo(),
                    tren.getEstatus(),
                    tren.getCapacidadGeneral(),
                    tren.getCapacidadPrimeraClase(),
                    String.join(", ", tren.getServicios())
                });
            }

            lblCantidadTrenesRegistrados.setText(
                    trenes.size() + " trenes registrados"
            );

        } catch (ServicioException e) {
            JOptionPane.showMessageDialog(
                    this,
                    "No fue posible cargar los trenes.\n"
                    + e.getMessage(),
                    "Error al cargar trenes",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }

    /**
     * Carga en el combo los servicios previamente registrados en otros trenes.
     */
    private void cargarServiciosDisponibles() {
        try {
            ITrenServicio trenServicio = new TrenServicio();
            List<String> servicios = trenServicio.consultarServiciosDisponibles();

            DefaultComboBoxModel<String> modeloCombo
                    = new DefaultComboBoxModel<>();

            for (String servicio : servicios) {
                modeloCombo.addElement(servicio);
            }

            cmbServicioTren.setModel(modeloCombo);
            cmbServicioTren.setSelectedItem(null);

        } catch (ServicioException e) {
            JOptionPane.showMessageDialog(
                    this,
                    "No fue posible cargar los servicios disponibles.\n"
                    + e.getMessage(),
                    "Error al cargar servicios",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }

    /**
     * Obtiene los servicios asignados al tren desde la lista visual.
     *
     * @return Lista de servicios capturados.
     */
    private List<String> obtenerServiciosTren() {
        List<String> servicios = new ArrayList<>();

        for (int i = 0; i < modeloServiciosTren.size(); i++) {
            servicios.add(modeloServiciosTren.getElementAt(i));
        }

        return servicios;
    }

