    private boolean validarFormulario() {
        if (txtCodigoTren.getText().trim().isBlank()) {
            JOptionPane.showMessageDialog(this,
                    "Debe capturar el código del tren.",
                    "Validación",
                    JOptionPane.WARNING_MESSAGE);
            txtCodigoTren.requestFocus();
            return false;
        }

        if (txtNombreTren.getText().trim().isBlank()) {
            JOptionPane.showMessageDialog(this,
                    "Debe capturar el nombre del tren.",
                    "Validación",
                    JOptionPane.WARNING_MESSAGE);
            txtNombreTren.requestFocus();
            return false;
        }

        if (txtModeloTren.getText().trim().isBlank()) {
            JOptionPane.showMessageDialog(this,
                    "Debe capturar el modelo del tren.",
                    "Validación",
                    JOptionPane.WARNING_MESSAGE);
            txtModeloTren.requestFocus();
            return false;
        }

        if (txtCapacidadGeneralTren.getText().trim().isBlank()) {
            JOptionPane.showMessageDialog(this,
                    "Debe capturar la capacidad general.",
                    "Validación",
                    JOptionPane.WARNING_MESSAGE);
            txtCapacidadGeneralTren.requestFocus();
            return false;
        }

        if (txtCapacidadPrimeraTren.getText().trim().isBlank()) {
            JOptionPane.showMessageDialog(this,
                    "Debe capturar la capacidad de primera clase.",
                    "Validación",
                    JOptionPane.WARNING_MESSAGE);
            txtCapacidadPrimeraTren.requestFocus();
            return false;
        }

        try {
            int capacidadGeneral = Integer.parseInt(txtCapacidadGeneralTren.getText().trim());
            int capacidadPrimera = Integer.parseInt(txtCapacidadPrimeraTren.getText().trim());

            if (capacidadGeneral <= 0) {
                JOptionPane.showMessageDialog(this,
                        "La capacidad general debe ser mayor a cero.",
                        "Validación",
                        JOptionPane.WARNING_MESSAGE);
                txtCapacidadGeneralTren.requestFocus();
                return false;
            }

            if (capacidadPrimera < 0) {
                JOptionPane.showMessageDialog(this,
                        "La capacidad de primera clase no puede ser negativa.",
                        "Validación",
                        JOptionPane.WARNING_MESSAGE);
                txtCapacidadPrimeraTren.requestFocus();
                return false;
            }

            if (capacidadPrimera > capacidadGeneral) {
                JOptionPane.showMessageDialog(this,
                        "La capacidad de primera clase no puede ser mayor que la capacidad general.",
                        "Validación",
                        JOptionPane.WARNING_MESSAGE);
                txtCapacidadPrimeraTren.requestFocus();
                return false;
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                    "Las capacidades deben ser numéricas.",
                    "Validación",
                    JOptionPane.WARNING_MESSAGE);
            return false;
        }

        if (modeloServiciosTren.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Debe agregar al menos un servicio.",
                    "Validación",
                    JOptionPane.WARNING_MESSAGE);
            cmbServicioTren.requestFocus();
            return false;
        }

        return true;
    }

