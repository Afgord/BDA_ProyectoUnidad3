    /**
     * Carga una fecha ISO-8601 en los componentes gráficos de fecha y hora.
     *
     * @param fechaHoraIso Fecha y hora almacenada en formato ISO-8601.
     * @param dateChooser Componente de fecha.
     * @param spinnerHora Componente de hora.
     */
    private void cargarFechaHoraEnComponentes(String fechaHoraIso,
            JDateChooser dateChooser,
            JSpinner spinnerHora) {

        if (fechaHoraIso == null || fechaHoraIso.isBlank()) {
            dateChooser.setDate(null);
            reiniciarSpinnerHora(spinnerHora);
            return;
        }

        try {
            Instant instant = Instant.parse(fechaHoraIso);
            ZonedDateTime fechaHoraLocal = instant.atZone(ZoneId.systemDefault());

            Date fechaParaDateChooser = Date.from(
                    fechaHoraLocal.toLocalDate()
                            .atStartOfDay(ZoneId.systemDefault())
                            .toInstant()
            );

            dateChooser.setDate(fechaParaDateChooser);
            spinnerHora.setValue(Date.from(fechaHoraLocal.toInstant()));

        } catch (DateTimeParseException e) {
            dateChooser.setDate(null);
            reiniciarSpinnerHora(spinnerHora);
        }
    }

    /**
     * Obtiene la fecha actual con la hora establecida en 00:00:00.
     *
     * @return Fecha actual sin información de hora.
     */
    private Date obtenerFechaActualSinHora() {
        return obtenerFechaSinHora(new Date());
    }

    /**
     * Elimina la información de hora de una fecha.
     *
     * @param fecha Fecha a normalizar.
     * @return Fecha con hora 00:00:00.
     */
    private Date obtenerFechaSinHora(Date fecha) {
        Calendar calendario = Calendar.getInstance();
        calendario.setTime(fecha);
        calendario.set(Calendar.HOUR_OF_DAY, 0);
        calendario.set(Calendar.MINUTE, 0);
        calendario.set(Calendar.SECOND, 0);
        calendario.set(Calendar.MILLISECOND, 0);

        return calendario.getTime();
    }

    /**
     * Ajusta la fecha mínima seleccionable para la llegada.
     *
     * Si existe fecha de salida, la llegada no puede ser anterior a esa fecha.
     * Si no existe fecha de salida, la llegada no puede ser anterior al día
     * actual.
     */
    private void actualizarFechaMinimaLlegada() {
        Date fechaSalida = dchFechaSalidaViaje.getDate();

        Date fechaMinimaLlegada;

        if (fechaSalida != null) {
            fechaMinimaLlegada = obtenerFechaSinHora(fechaSalida);
        } else {
            fechaMinimaLlegada = obtenerFechaActualSinHora();
        }

        dchFechaLlegadaViaje.setMinSelectableDate(fechaMinimaLlegada);

        Date fechaLlegadaSeleccionada = dchFechaLlegadaViaje.getDate();

        /*
     * Si ya había una fecha de llegada elegida y ahora queda fuera del rango
     * permitido, se limpia para obligar a seleccionar una fecha válida.
         */
        if (fechaLlegadaSeleccionada != null
                && obtenerFechaSinHora(fechaLlegadaSeleccionada)
                        .before(fechaMinimaLlegada)) {

            dchFechaLlegadaViaje.setDate(null);
        }
    }

    /**
     * Convierte una fecha ISO-8601 almacenada en UTC a formato local para
     * mostrarla de forma amigable en la interfaz.
     *
     * @param fechaHoraIso Fecha y hora en formato ISO-8601.
     * @return Fecha formateada como dd-MM-yyyy HH:mm.
     */
    private String formatearFechaHoraParaTabla(String fechaHoraIso) {
        if (fechaHoraIso == null || fechaHoraIso.isBlank()) {
            return "";
        }

        try {
            Instant instant = Instant.parse(fechaHoraIso);

            ZonedDateTime fechaHoraLocal = instant.atZone(
                    ZoneId.systemDefault()
            );

            DateTimeFormatter formatoTabla
                    = DateTimeFormatter.ofPattern("dd-MM-yyyy HH:mm");

            return fechaHoraLocal.format(formatoTabla);

        } catch (DateTimeParseException e) {
            return fechaHoraIso;
        }
    }

