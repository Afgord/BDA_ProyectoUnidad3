    /**
     * Carga los viajes registrados en la tabla.
     */
    private void cargarViajes() {
        try {
            IViajeServicio viajeServicio = new ViajeServicio();
            List<ViajeDTO> viajes = viajeServicio.consultarTodos();

            DefaultTableModel modelo
                    = (DefaultTableModel) tblListaViajes.getModel();

            modelo.setRowCount(0);

            for (ViajeDTO viaje : viajes) {
                modelo.addRow(new Object[]{
                    viaje.getId(),
                    viaje.getTrenNombre(),
                    viaje.getOrigenNombre(),
                    viaje.getDestinoNombre(),
                    formatearFechaHoraParaTabla(viaje.getFechaHoraSalida()),
                    viaje.getEstatus(),
                    viaje.getDisponibilidadGeneral(),
                    viaje.getDisponibilidadPrimeraClase()
                });
            }

            lblViajesProgramados.setText(
                    viajes.size() + " viajes registrados"
            );

        } catch (ServicioException e) {
            JOptionPane.showMessageDialog(
                    this,
                    "No fue posible cargar los viajes.\n"
                    + e.getMessage(),
                    "Error al cargar viajes",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }

