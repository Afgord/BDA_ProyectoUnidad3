    /**
     * Registra un nuevo viaje o actualiza uno existente.
     */
    private void btnProgramarViajeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProgramarViajeActionPerformed
        if (!validarFormulario()) {
            return;
        }

        try {
            IViajeServicio viajeServicio = new ViajeServicio();

            TrenDTO trenSeleccionado = obtenerTrenSeleccionado();
            LocacionDTO origenSeleccionado = obtenerOrigenSeleccionado();
            LocacionDTO destinoSeleccionado = obtenerDestinoSeleccionado();

            ViajeDTO viajeDTO = new ViajeDTO();

            viajeDTO.setTrenId(trenSeleccionado.getId());
            viajeDTO.setOrigenId(origenSeleccionado.getId());
            viajeDTO.setDestinoId(destinoSeleccionado.getId());

            viajeDTO.setFechaHoraSalida(
                    construirFechaHoraIso(
                            dchFechaSalidaViaje.getDate(),
                            (Date) spnHoraSalidaViaje.getValue()
                    )
            );

            viajeDTO.setFechaHoraLlegadaEstimada(
                    construirFechaHoraIso(
                            dchFechaLlegadaViaje.getDate(),
                            (Date) spnHoraLlegadaViaje.getValue()
                    )
            );

            viajeDTO.setEstatus(cmbEstatusViaje.getSelectedItem().toString());

            viajeDTO.setCapacidadMaximaGeneral(
                    Integer.parseInt(txtCapacidadMaxGeneralViaje.getText().trim())
            );

            viajeDTO.setCapacidadMaximaPrimeraClase(
                    Integer.parseInt(txtCapacidadMaxPrimeraViaje.getText().trim())
            );

            viajeDTO.setDisponibilidadGeneral(
                    Integer.parseInt(txtDisponibilidadGeneralViaje.getText().trim())
            );

            viajeDTO.setDisponibilidadPrimeraClase(
                    Integer.parseInt(txtDisponibilidadPrimeraViaje.getText().trim())
            );

            if (!modoEdicion) {
                viajeServicio.registrar(viajeDTO);

                JOptionPane.showMessageDialog(
                        this,
                        "Viaje programado correctamente.",
                        "Registro exitoso",
                        JOptionPane.INFORMATION_MESSAGE
                );

            } else {
                viajeDTO.setId(idViajeSeleccionado);
                viajeServicio.actualizar(viajeDTO);

                JOptionPane.showMessageDialog(
                        this,
                        "Viaje actualizado correctamente.",
                        "Actualización exitosa",
                        JOptionPane.INFORMATION_MESSAGE
                );
            }

            cargarViajes();
            limpiarFormulario();

        } catch (ServicioException e) {
            JOptionPane.showMessageDialog(
                    this,
                    e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_btnProgramarViajeActionPerformed

