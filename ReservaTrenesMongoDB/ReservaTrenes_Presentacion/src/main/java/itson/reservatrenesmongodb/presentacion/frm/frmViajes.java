    /**
     * Carga un viaje seleccionado de la tabla al formulario para editarlo.
     */
    private void btnEditarViajeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarViajeActionPerformed
        int fila = tblListaViajes.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(
                    this,
                    "Seleccione un viaje de la tabla.",
                    "Sin selección",
                    JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        try {
            IViajeServicio viajeServicio = new ViajeServicio();

            String id = tblListaViajes.getValueAt(fila, 0).toString();
            ViajeDTO viaje = viajeServicio.buscarPorId(id);

            if (viaje == null) {
                JOptionPane.showMessageDialog(
                        this,
                        "No fue posible encontrar el viaje seleccionado.",
                        "Viaje no encontrado",
                        JOptionPane.WARNING_MESSAGE
                );

                cargarViajes();
                return;
            }

            idViajeSeleccionado = viaje.getId();
            modoEdicion = true;

            seleccionarTrenEnCombo(viaje.getTrenId());
            seleccionarLocacionOrigenEnCombo(viaje.getOrigenId());
            seleccionarLocacionDestinoEnCombo(viaje.getDestinoId());
            cmbEstatusViaje.setSelectedItem(viaje.getEstatus());

            cargarFechaHoraEnComponentes(
                    viaje.getFechaHoraSalida(),
                    dchFechaSalidaViaje,
                    spnHoraSalidaViaje
            );

            cargarFechaHoraEnComponentes(
                    viaje.getFechaHoraLlegadaEstimada(),
                    dchFechaLlegadaViaje,
                    spnHoraLlegadaViaje
            );

            txtCapacidadMaxGeneralViaje.setText(
                    String.valueOf(viaje.getCapacidadMaximaGeneral())
            );

            txtCapacidadMaxPrimeraViaje.setText(
                    String.valueOf(viaje.getCapacidadMaximaPrimeraClase())
            );

            txtDisponibilidadGeneralViaje.setText(
                    String.valueOf(viaje.getDisponibilidadGeneral())
            );

            txtDisponibilidadPrimeraViaje.setText(
                    String.valueOf(viaje.getDisponibilidadPrimeraClase())
            );

            btnProgramarViaje.setText("Actualizar viaje");
            lblProgramarNuevoViaje.setText("Editar viaje");

        } catch (ServicioException e) {
            JOptionPane.showMessageDialog(
                    this,
                    e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_btnEditarViajeActionPerformed

